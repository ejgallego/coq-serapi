open Sexplib.Std
open Serlib

module Names        = Ser_names
module Deprecation  = Ser_deprecation

module Ltac_plugin = struct
  module Tacexpr = Ser_tacexpr
  module Tacenv  = Ltac_plugin.Tacenv
end

open Ltac_plugin [@@ocaml.warning "-33"]

type ltac_entry =
  [%import: Ltac_plugin.Tacenv.ltac_entry]
  [@@deriving sexp]

let ltac_entry_of_python =
  Serlib_base.opaque_of_python ~typ:"ltac_entry"

let python_of_ltac_entry =
  Serlib_base.python_of_opaque ~typ:"ltac_entry"
